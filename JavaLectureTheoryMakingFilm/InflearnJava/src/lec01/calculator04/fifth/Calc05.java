package lec01.calculator04.fifth;

public class Calc05 {
	
	public static void main(String[] args) {
		
		int one = 10;
		int two = 5;
		
		boolean cmpOne = one > two;
		System.out.println("cmpOne : " + cmpOne);
		
		cmpOne = one < two;
		System.out.println("cmpOne : " + cmpOne);
		
		cmpOne = one == two;
		System.out.println("cmpOne : " + cmpOne);
		
		cmpOne = one != two;
		System.out.println("cmpOne : " + cmpOne);
		
	}

}
